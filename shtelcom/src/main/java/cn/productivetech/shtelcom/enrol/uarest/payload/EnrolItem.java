package cn.productivetech.shtelcom.enrol.uarest.payload;

public class EnrolItem {

	private String			type;
	private String			numberModelUpdates;
	private EnrolGenerated	generatedBy;
	private String lastUpdateDateYYYYMMDD;

	public String getLastUpdateDateYYYYMMDD() {
		return lastUpdateDateYYYYMMDD;
	}

	public void setLastUpdateDateYYYYMMDD(String lastUpdateDateYYYYMMDD) {
		this.lastUpdateDateYYYYMMDD = lastUpdateDateYYYYMMDD;
	}
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getNumberModelUpdates() {
		return numberModelUpdates;
	}

	public void setNumberModelUpdates(String numberModelUpdates) {
		this.numberModelUpdates = numberModelUpdates;
	}

	public EnrolGenerated getGeneratedBy() {
		return generatedBy;
	}

	public void setGeneratedBy(EnrolGenerated generatedBy) {
		this.generatedBy = generatedBy;
	}

}
