package cn.productivetech.shtelcom.enrol.uarest.payload;

public class MetaInformation {

	private String key;
	private MetaInformationValue value;

	public MetaInformation() {
		this.key = "usage-context";
		this.value = new MetaInformationValue();
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public MetaInformationValue getValue() {
		return value;
	}

	public void setValue(MetaInformationValue value) {
		this.value = value;
	}

}
