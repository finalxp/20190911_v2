package cn.productivetech.shtelcom.enrol.uarest.core;

public class UABiometric {
	
	/**
	 * 语音质量检测
	 */
	public final static String DETECT_SPEECH = "detect-speech";
	/**
	 * 信噪比检测
	 */
	public final static String GET_SNR = "get-snr";
	/**
	 * 重播检测 默认引擎
	 */
	public final static String DETECT_REPLAY_MODE = "detect-replay";
	/**
	 * 重播检测 8K引擎,对应8K采样率
	 */
	public final static String DETECT_REPLAY_MODE_8K = "detect-replay-v2-8k";
	/**
	 * 重播检测16K引擎,对应16K采样率
	 */
	public final static String DETECT_REPLAY_MODE_16K = "detect-replay-v2-16k";

	/**
	 * 固定文本8K引擎,对应8K采样率
	 */
	public final static String BIOMETRIC_TYPE_TD_MODE_8K = "td_fuse_8_atn_v2";
	/**
	 * 固定文本16K引擎,对应16K采样率
	 */
	public final static String BIOMETRIC_TYPE_TD_MODE_16K = "td_fuse_16_atn_v2";
	/**
	 * 自由说引擎,8K & 16K都用此模式
	 */
	public final static String BIOMETRIC_TYPE_TI_MODE = "ti_plp2covv2";
	

	public final static String BIOMETRIC_TYPE_TI = "text-independent";
	public final static String BIOMETRIC_TYPE_TD = "text-dependent";

	private String biometricMode;
	private String detectReplayMode;
	
	private String biometricType;

	public String getBiometricMode() {
		return biometricMode;
	}

	public void setBiometricMode(String biometricMode) {
		this.biometricMode = biometricMode;
	}

	public String getDetectReplayMode() {
		return detectReplayMode;
	}

	public void setDetectReplayMode(String detectReplayMode) {
		this.detectReplayMode = detectReplayMode;
	}

	public String getBiometricType() {
		return biometricType;
	}

	public void setBiometricType(String biometricType) {
		this.biometricType = biometricType;
	}

}
