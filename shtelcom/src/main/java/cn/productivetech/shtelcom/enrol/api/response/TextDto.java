package cn.productivetech.shtelcom.enrol.api.response;

import cn.productivetech.shtelcom.enrol.model.TextBean;

public class TextDto {

	private String textId;

	private String textWord;

	public TextDto() {
	}

	public TextDto(TextBean bean) {
		this.textId = bean.getTextId();
		this.textWord = bean.getTextWord();
	}

	public String getTextId() {
		return textId;
	}

	public void setTextId(String textId) {
		this.textId = textId;
	}

	public String getTextWord() {
		return textWord;
	}

	public void setTextWord(String textWord) {
		this.textWord = textWord;
	}

}
