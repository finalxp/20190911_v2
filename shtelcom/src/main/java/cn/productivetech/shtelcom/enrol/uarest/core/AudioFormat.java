package cn.productivetech.shtelcom.enrol.uarest.core;

public class AudioFormat {

	/**
	 * pcm16编码
	 */
	public final static String AUDIO_FORMAT_PCM = "pcm16";
	/**
	 * g711 alaw编码
	 */
	public final static String AUDIO_FORMAT_ALAW = "alaw";
	/**
	 * g711 ulaw 编码
	 */
	public final static String AUDIO_FORMAT_MULAW = "ulaw";
	/**
	 * mp3编码
	 */
	public final static String AUDIO_FORMAT_MP3 = "mp3";

	/**
	 * 8K采样率
	 */
	public final static int AUDIO_SAMPLINGRATE_8K = 8000;
	/**
	 * 16k采样率
	 */
	public final static int AUDIO_SAMPLINGRATE_16K = 16000;

	private String format;
	private int samplingRate;

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public int getSamplingRate() {
		return samplingRate;
	}

	public void setSamplingRate(int samplingRate) {
		this.samplingRate = samplingRate;
	}
}
