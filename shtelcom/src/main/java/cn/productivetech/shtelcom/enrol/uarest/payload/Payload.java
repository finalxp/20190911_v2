package cn.productivetech.shtelcom.enrol.uarest.payload;

public class Payload {

	private ServiceData				serviceData;
	private UserData				userData;
	private ProcessingInformation	processingInformation;
	private AudioInput				audioInput;

	public Payload() {
		this.serviceData = new ServiceData();
		this.userData = new UserData();
		this.processingInformation = new ProcessingInformation();
		this.audioInput = new AudioInput();
	}

	public ServiceData getServiceData() {
		return serviceData;
	}

	public void setServiceData(ServiceData serviceData) {
		this.serviceData = serviceData;
	}

	public UserData getUserData() {
		return userData;
	}

	public void setUserData(UserData userData) {
		this.userData = userData;
	}

	public ProcessingInformation getProcessingInformation() {
		return processingInformation;
	}

	public void setProcessingInformation(ProcessingInformation processingInformation) {
		this.processingInformation = processingInformation;
	}

	public AudioInput getAudioInput() {
		return audioInput;
	}

	public void setAudioInput(AudioInput audioInput) {
		this.audioInput = audioInput;
	}

}
