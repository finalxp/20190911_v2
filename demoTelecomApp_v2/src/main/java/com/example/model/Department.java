package com.example.model;

public class Department {
	private String id;
	private String department_id;
	private String department_name;
	private String department_type;
	private String department_sup_id;
	private String department_sup_name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDepartment_id() {
		return department_id;
	}
	public void setDepartment_id(String department_id) {
		this.department_id = department_id;
	}
	public String getDepartment_name() {
		return department_name;
	}
	public void setDepartment_name(String department_name) {
		this.department_name = department_name;
	}
	public String getDepartment_type() {
		return department_type;
	}
	public void setDepartment_type(String department_type) {
		this.department_type = department_type;
	}
	public String getDepartment_sup_id() {
		return department_sup_id;
	}
	public void setDepartment_sup_id(String department_sup_id) {
		this.department_sup_id = department_sup_id;
	}
	public String getDepartment_sup_name() {
		return department_sup_name;
	}
	public void setDepartment_sup_name(String department_sup_name) {
		this.department_sup_name = department_sup_name;
	}

}
